a = 8 # This is the A Value 
b = 2 # This is the B Value 

print( 'Addition:\t' , a , '+' , b , '=' , a + b) # Adds A & B values and displays them

print( 'Subtraction:\t' , a , '-' , b , '=' , a - b) # Subtracts A & B values and displays them

print( 'Multiplication:\t' , a , 'x' , b , '=' , a * b) # Multiplies A & B values and displays them

print( 'Division:\t' , a , '/' , b , '=' , a / b) # Divides A & B values and displays them

print( 'Floor Division:\t' , a , '/' , b , '=' , a // b)  # Floor Divides A & B values and displays them

print( 'Modulo:\t' , a , '%' , b , '=' , a % b) # Modulos A & B values and displays them

print( 'Exponent:\t' , a , ' ^2 ' , a ** b, sep = '') # Converts A & B values to an exponent and displays them 
