nil = 0 # Nil aka null varible 
num = 0 # Number varible 
max = 1 # Maximum 1 varible 
cap = 'A' # Uppercase 'A' varible 
low = 'a' # Lowercase 'a' varible

print( 'Equality :\t' , nil , '==' , num , nil == num ) # Number eqaulity
print( 'Equality :\t' , cap , '==' , low , cap == low ) # Letter eqaulity
print( 'Inequality :\t' , nil , '!=' , max , nil != max ) # Inequality 
print( 'Greater :\t' , nil , '>' , max , nil > max ) # Greater than equality
print( 'Lesser :\t' , nil , '<' , max , nil < max ) # Less than equality 
print( 'More or Equal :\t' , nil , '>=' , num , nil >= num ) # More or equal
print( 'Less or Equal :\t' , max , '<=' , num , max <= num ) # Less or equal 