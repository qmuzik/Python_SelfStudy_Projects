i = 1 # Starts counter and defines a outer loop
while i < 4:

	print( '\nOuter Loop Iteration:' , i ) # Displays the counters value and increment for each loop 
	i += 1
	
	j = 1 # Starts second counter and define inner loop 
	while j < 4:

		print( '\tInner Loop Iteration:' , j ) # Displays the counters value and increment 
		j += 1
